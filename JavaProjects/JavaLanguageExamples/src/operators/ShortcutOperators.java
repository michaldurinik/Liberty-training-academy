package operators;

public class ShortcutOperators {
    public static void main(String[] args) {
        int myvar1 = 16;
        int myvar2 = 36;

        myvar1 += 5;
        System.out.println(myvar1);

        myvar1 -= 8;
        System.out.println(myvar1);

        myvar2 *= 2;
        System.out.println(myvar2);

        myvar2 >>= 3;
        System.out.println(myvar2);
    }
}
