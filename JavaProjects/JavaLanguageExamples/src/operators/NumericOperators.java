package operators;

public class NumericOperators {
    public static void main(String[] args) {
        int myvar1 = 16;
        int myvar2 = 36;

        System.out.println(myvar1 + myvar2);
        System.out.println(myvar1 - myvar2);
        System.out.println(myvar1 * myvar2);
        System.out.println(myvar2 / myvar1);
        System.out.println(myvar2 % myvar1);
        System.out.println(myvar1 << 2);
        System.out.println(myvar1 >> 2);
        System.out.println(myvar1 | myvar2);
        System.out.println(myvar1 ^ myvar2);
        System.out.println(myvar1 & myvar2);
    }
}

